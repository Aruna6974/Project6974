package Operators;

public class Assignment {

	public static void main(String[] args) 
	{
		int X=14,Y=12,Z=18;
		System.out.println("The Assignment Value is :");
		X+=2;//(X=X+2)
		Y-=2;//(Y=Y-2)
		Z*=2;//(Z=Z*2)
		System.out.println("The value of X is:" +X);//16
		System.out.println("The value of Y is:"+Y);//10
		System.out.println("The value of Z is:"+Z);//36

	}

}
